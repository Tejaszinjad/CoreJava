package Revision;

public class Book {
	
	private String title;
	private String author;
	private int isbn;
	
	
	public String getTitle() {
		return this.title;
	}
	
	public void setTitle(String title) {
		this.title=title;
	}
	public String getAuthor() {
		return this.author;
	}
	public void setAuthor(String author) {
		this.author=author;
	}
	public int getIsbn() {
		return this.isbn;
	}
	public void setIsbn(int isbn) {
		this.isbn=isbn;
	}
}
